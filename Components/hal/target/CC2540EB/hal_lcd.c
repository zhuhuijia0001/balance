/*********************LCD 延时1ms************************************/
void LCD_DLY_ms(unsigned int ms)
{                         
    unsigned int a;
    while(ms)
    {
        a=1800;
        while(a--);
        ms--;
    }
    return;
}

#if SPI_MODE == USE_HARD_SPI
static void LCD_WrByte(unsigned char c)
{
  	UxDBUF = c;
	while (UxCSR & (1 << 0));
}

#else
static void LCD_WrByte(unsigned char c)
{
    unsigned char i;
    for (i = 0; i < 8; i++) //发送一个八位数据 
    {
        LCD_SCL = 0;  

        LCD_SDA = (c & 0x80) != 0;

        c <<= 1;
        LCD_SCL = 1;
    }
}

#endif



/*********************LCD写数据************************************/ 
void LCD_WrDat(unsigned char dat)     
{
    LCD_DC = 1;  
    LCD_WrByte(dat);
}
/*********************LCD写命令************************************/                                        
void LCD_WrCmd(unsigned char cmd)
{
    LCD_DC = 0;  
    LCD_WrByte(cmd);    
}
/*********************LCD 设置坐标************************************/
void LCD_Set_Pos(unsigned char x, unsigned char y) 
{ 
    LCD_WrCmd(0xb0+y);
    LCD_WrCmd(((x&0xf0)>>4)|0x10);
    LCD_WrCmd((x&0x0f)|0x01); 
} 
/*********************LCD全屏************************************/
void LCD_Fill(unsigned char bmp_dat) 
{
    unsigned char y,x;
    for(y=0;y<8;y++)
    {
        LCD_WrCmd(0xb0+y);
        LCD_WrCmd(0x01);
        LCD_WrCmd(0x10);
        for(x=0;x<X_WIDTH;x++)
            LCD_WrDat(bmp_dat);
    }
}

/***************功能描述：行填充, y为页范围0～7****************/
void LCD_FillLine(unsigned char y,unsigned char ch)
{
    unsigned char x;    
    LCD_WrCmd(0xb0+y);
    LCD_WrCmd(0x01);
    LCD_WrCmd(0x10); 
    for(x=0;x<X_WIDTH;x++)
        LCD_WrDat(ch);
}

/*********************LCD复位************************************/
void LCD_CLS(void)
{
    unsigned char y,x;    
    for(y=0;y<8;y++)
    {
        LCD_WrCmd(0xb0+y);
        LCD_WrCmd(0x01);
        LCD_WrCmd(0x10); 
        for(x=0;x<X_WIDTH;x++)
            LCD_WrDat(0);
    }
}
